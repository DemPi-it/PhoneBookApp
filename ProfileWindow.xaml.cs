namespace PhoneBookApp
{
    /// <summary>
    /// Логика взаимодействия для ProfileWindow.xaml
    /// </summary>
    public partial class ProfileWindow : Window
    {
        public ProfileWindow()
        {
            InitializeComponent();
            usernameProfile.Text = SystemInformation.UserName;
            domainName.Text = SystemInformation.UserDomainName;
        }

        private void Grid_Loaded(object sender, RoutedEventArgs e) // Method from the forum showing the current time
        {
            DispatcherTimer timer = new DispatcherTimer(TimeSpan.FromSeconds(1), DispatcherPriority.Normal, (object s, EventArgs ev) =>
            {
                this.myDateTime.Text = DateTime.Now.ToString("yyyy/MM/dd hh:mm:ss");
            }, this.Dispatcher);
            timer.Start();
        }
    }
}
